#include "../includes/ft_printf.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = -1;
	while (s[++i] != (char)c)
		if (s[i] == 0)
			return (NULL);
	return ((char *)&s[i]);
}
